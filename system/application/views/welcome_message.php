<h1>User Guide</h1>

<p>This library has been provided by Leon Barrett (<a href="http://www.twitter.com/leonbarrett">@leonbarrett</a>) to provide a simple and clean way to interact with the PayPal NVP API. It builds upon concepts from the Code Igniter PayPal Library (v. 0.1) by <a href="http://aroussi.com/ci/paypal_lib/">http://aroussi.com/ci/paypal_lib</a>.</p>

<p>This library assumes that you have some prior knowledge of the PayPal API. More information can be found on the <a href="https://cms.paypal.com/us/cgi-bin/?cmd=_render-content&content_ID=developer/howto_api_reference" target="_blank">PayPal API reference pages</a>.</p>

<p>This package consists of the following files</p>

<ul>
	<li>Config > autoload.php - Use to auto load the paypal_lib.php and my paypal_api_lib.php libraries, the paypal_helper.php helper and the paypallib_config config file.</li>
	<li>Controllers > demo.php used to demonstrate using the library to display the balance of the current PayPal account.</li>
</ul>

<p>If you would like to edit this page you'll find it located at:</p>
<code>system/application/views/welcome_message.php</code>

<p>The corresponding controller for this page is found at:</p>
<code>system/application/controllers/welcome.php</code>

<p>If you are exploring CodeIgniter for the very first time, you should start by reading the <a href="user_guide/">User Guide</a>.</p>


